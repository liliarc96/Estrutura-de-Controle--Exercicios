package com.gasolina;

public class Motorista {
	public double capacidadeTanque;
	public double qntGasolina;
	public double despesaFinal;
	
	public double getCapacidadeTanque() {
		return capacidadeTanque;
	}
	public void setCapacidadeTanque(double capacidadeTanque) {
		this.capacidadeTanque = capacidadeTanque;
	}
	public double getQntGasolina() {
		return qntGasolina;
	}
	public void setQntGasolina(double qntGasolina) {
		this.qntGasolina = qntGasolina;
	}
	public double getDespesaFinal() {
		return despesaFinal;
	}
	public void setDespesaFinal(double despesaFinal) {
		this.despesaFinal = despesaFinal;
	}
}
